    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        if(rooms==null || location.getText()=="" || rooms.getText()=="" || ameneties.getText()=="" || rent.getText()=="" ||area.getText()==""){
        
           JOptionPane.showMessageDialog(rootPane, "All the fields should be filled");
        }
            
         House property=new House(images,location.getText(),Integer.parseInt(rooms.getText()),ameneties.getText(),Integer.parseInt(rent.getText()),Integer.parseInt(area.getText()));
         HouseCRUD houseCRUD =new HouseCRUD();
         houseCRUD.insert(property);
         JOptionPane.showMessageDialog(rootPane, "Successfully saved");
         rooms=null;
         
         location.setText("");
         rooms.setText("");
         ameneties.setText("");
         rent.setText("");
         area.setText("");
    }//GEN-LAST:event_saveActionPerformed

