    private void addImagesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addImagesActionPerformed
        // TODO add your handling code here:
        JFileChooser file = new JFileChooser();
                file.setMultiSelectionEnabled(true);
                FileNameExtensionFilter fnef = new FileNameExtensionFilter("image", "png","jpeg","jpg");
                file.addChoosableFileFilter(fnef);
                if (file.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {

                    File[] chfile = file.getSelectedFiles();
                    images=new ArrayList<>();
                    for(int i=0;i<chfile.length;i++){
                        RoomImage roomImage=new RoomImage();
                        roomImage.setImage(chfile[i].getAbsolutePath());
                        images.add(roomImage);

                    }
                }
    }//GEN-LAST:event_addImagesActionPerformed

